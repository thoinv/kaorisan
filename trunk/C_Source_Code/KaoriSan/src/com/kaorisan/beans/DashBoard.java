package com.kaorisan.beans;

public class DashBoard {
	private int openTask;
	private int closedTask;
	private int availableTask;
	
	public int getOpenTask() {
		return openTask;
	}
	public void setOpenTask(int openTask) {
		this.openTask = openTask;
	}
	public int getClosedTask() {
		return closedTask;
	}
	public void setClosedTask(int closedTask) {
		this.closedTask = closedTask;
	}
	public int getAvailableTask() {
		return availableTask;
	}
	public void setAvailableTask(int availableTask) {
		this.availableTask = availableTask;
	}
	
	
}
